using System;
using LuaInterface;

public class bitWrap
{
	public static void Register(IntPtr L)
	{
		LuaMethod[] regs = new LuaMethod[]
		{
			new LuaMethod("band", band),
			new LuaMethod("bor", bor),
			new LuaMethod("bnot", bnot),
			new LuaMethod("bxor", bxor),
			new LuaMethod("lshift", lshift),
			new LuaMethod("rshift", rshift),
			new LuaMethod("to0x", to0x),
			new LuaMethod("New", _Createbit),
			new LuaMethod("GetClassType", GetClassType),
		};

		LuaScriptMgr.RegisterLib(L, "bit", regs);
	}

	[MonoPInvokeCallbackAttribute(typeof(LuaCSFunction))]
	static int _Createbit(IntPtr L)
	{
		LuaDLL.luaL_error(L, "bit class does not have a constructor function");
		return 0;
	}

	static Type classType = typeof(bit);

	[MonoPInvokeCallbackAttribute(typeof(LuaCSFunction))]
	static int GetClassType(IntPtr L)
	{
		LuaScriptMgr.Push(L, classType);
		return 1;
	}

	[MonoPInvokeCallbackAttribute(typeof(LuaCSFunction))]
	static int band(IntPtr L)
	{
		LuaScriptMgr.CheckArgsCount(L, 2);
		int arg0 = (int)LuaScriptMgr.GetNumber(L, 1);
		int arg1 = (int)LuaScriptMgr.GetNumber(L, 2);
		int o = bit.band(arg0,arg1);
		LuaScriptMgr.Push(L, o);
		return 1;
	}

	[MonoPInvokeCallbackAttribute(typeof(LuaCSFunction))]
	static int bor(IntPtr L)
